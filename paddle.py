from turtle import Turtle


class Paddle(Turtle):
    """ A paddle class that inherits from Trurtl """

    def __init__(self, position):
        super().__init__()
        self.shape("square")
        self.color("white")
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.penup()
        self.goto(position)
